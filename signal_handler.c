#include "main.h"

/* ************************************************************************** */
void	handler_sigint(int sig_code)
{
	// printf("\n%s", MSG_PROMPT);
	write(1, MSG_SIGINT, strlen(MSG_SIGINT));
}

/* ************************************************************************** */
void	handler_sigquit(int sig_code)
{
	// printf("SIGQUIT\n");
	write(1, MSG_SIGQUIT, strlen(MSG_SIGQUIT));
}

