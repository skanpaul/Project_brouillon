/* ************************************************************************** */
bool is_child(pid_t fk_id)
{
	if (fk_id == FK_CHILD)
		return (true);	
	return (false);
}
/* ************************************************************************** */
bool is_parent(pid_t fk_id)
{
	if (fk_id != FK_CHILD && fk_id != FK_ERROR)
		return (true);	
	return (false);
}
